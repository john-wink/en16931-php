<?php

declare(strict_types=1);

namespace JohnWink\En16931\Tools;

use JohnWink\En16931\RuleSets;
use RuntimeException;

/**
 * Renders the rule-coverage documentation (COVERAGE.md + the README summary)
 * by diffing the rules registered in {@see RuleSets} against the official
 * reference list in resources/rules-reference.json (built by
 * tools/build-rules-reference.php from the EN 16931 / KoSIT Schematron).
 *
 * Guarded by tests/Feature/CoverageDocTest.php so the docs cannot drift.
 */
final class CoverageMatrix
{
    public const string README_START = '<!-- coverage:start — generated by tools/generate-coverage.php, do not edit -->';

    public const string README_END = '<!-- coverage:end -->';

    /**
     * TaxableSumRule registers as "BR-CAT-08" but reports one of these ids.
     */
    private const array CATEGORY_SUM_IDS = ['BR-S-08', 'BR-Z-08', 'BR-E-08', 'BR-AE-08', 'BR-IC-08', 'BR-G-08', 'BR-O-08'];

    /**
     * Rules whose implementation deviates from the official scope (🟡).
     *
     * @var array<string, string>
     */
    private const array PARTIAL = [
        'BR-AE-02' => 'Simplified: requires the Seller VAT identifier (BT-31) only; officially BT-31, BT-32 or the tax representative VAT identifier (BT-63) qualify — BT-32/BT-63 are not modelled yet.',
        'BR-AE-03' => 'Simplified: checks the Buyer VAT identifier for any AE usage; officially -03 is the document-allowance trigger (the buyer check for AE lines belongs to BR-AE-02).',
        'BR-CL-14' => 'Checked for the seller (BT-40) and buyer (BT-55) country only; BT-69 (tax representative) and BT-80 (deliver-to) are not modelled yet.',
    ];

    /**
     * Implemented rules with a noteworthy (deliberate) deviation (✅ + note).
     *
     * @var array<string, string>
     */
    private const array NOTES = [
        'BR-CO-16' => 'Severity: warning — the KoSIT/XRechnung configuration treats BR-CO-16 as informational; the pure EN 16931 artefacts flag it fatal.',
        'BR-CO-17' => 'Stricter than official: exact equality required, the official artefacts allow a ±0.01 rounding tolerance.',
        'BR-S-08' => 'Grouped per (category, rate) for every category; officially only S splits per rate — equivalent in practice since the other categories carry rate 0.',
        'BR-Z-08' => 'Grouped per (category, rate) for every category; officially only S splits per rate — equivalent in practice since the other categories carry rate 0.',
        'BR-E-08' => 'Grouped per (category, rate) for every category; officially only S splits per rate — equivalent in practice since the other categories carry rate 0.',
        'BR-AE-08' => 'Grouped per (category, rate) for every category; officially only S splits per rate — equivalent in practice since the other categories carry rate 0.',
        'BR-IC-08' => 'Grouped per (category, rate) for every category; officially only S splits per rate — equivalent in practice since the other categories carry rate 0.',
        'BR-G-08' => 'Grouped per (category, rate) for every category; officially only S splits per rate — equivalent in practice since the other categories carry rate 0.',
        'BR-O-08' => 'Grouped per (category, rate) for every category; officially only S splits per rate — equivalent in practice since the other categories carry rate 0.',
        'BR-Z-09' => 'A missing BT-117 passes (presence is owned by BR-46).',
        'BR-E-09' => 'A missing BT-117 passes (presence is owned by BR-46).',
        'BR-AE-09' => 'A missing BT-117 passes (presence is owned by BR-46).',
